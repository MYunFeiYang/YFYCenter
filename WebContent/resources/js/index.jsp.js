function shouye() {
	if (document.getElementById('shouye').style.display == 'none') {// 如果contment1是隐藏的
		document.getElementById('computer').style.display = 'none';
		document.getElementById('sofaware').style.display = 'none';
		document.getElementById('comnication').style.display = 'none';
		document.getElementById('publicresource').style.display = 'none';
		document.getElementById('person').style.display = 'none';
		document.getElementById('shouye').style.display = 'block';// contment1的display属性设置为block（显示）
	} else {// 如果contment1是显示的
		document.getElementById('shouye').style.display = 'none';// contment1的display属性设置为none（隐藏）
	}
}
